#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*result;
	size_t			len;
	unsigned int	index;

	if (!s || !f)
		return (NULL);
	len = ft_strlen(s);
	result = (char *)malloc(len + 1);
	if (!result)
		return (NULL);
	index = 0;
	while (index < len)
	{
		result[index] = f(index, s[index]);
		index++;
	}
	result[len] = 0;
	return (result);
}
